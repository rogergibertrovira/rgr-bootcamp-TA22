package models;

public class AsignadoA {
	private String id;
	private String proyectoId;
	private String cientificoId;

	public String getId() {
		return id;
	}

	public String getProyectoId() {
		return proyectoId;
	}

	public String getCientificoId() {
		return cientificoId;
	}

	public void setId(String id) {
		this.id = id;
	}

	public void setProyectoId(String proyectoId) {
		this.proyectoId = proyectoId;
	}

	public void setCientificoId(String cientificoId) {
		this.cientificoId = cientificoId;
	}

}
