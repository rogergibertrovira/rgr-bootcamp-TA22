package models;

public class Cientifico {

	private String dni;
	private String nomApels;

	public String getDni() {
		return dni;
	}

	public String getNomApels() {
		return nomApels;
	}

	public void setDni(String dni) {
		this.dni = dni;
	}

	public void setNomApels(String nomApels) {
		this.nomApels = nomApels;
	}

}
